  private void mostrarLista(List<Aula> lista) {
    DefaultTableModel tabla;
    tabla = (DefaultTableModel) tblTabla.getModel();
    tabla.setRowCount(0);
    for (Aula aula : lista) {
      Object[] rowData = {
        aula.getCodigo(),
        aula.getDescripcion(),
      };
      tabla.addRow(rowData);
    }

  }
