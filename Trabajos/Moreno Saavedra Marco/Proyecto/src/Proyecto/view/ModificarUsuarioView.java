    public void creandoIcono(Image i) {
        ImageIcon icon = new ImageIcon(i);
        Image img = icon.getImage();
        Image newimg = img.getScaledInstance(138, 149, java.awt.Image.SCALE_SMOOTH);
        ImageIcon newIcon = new ImageIcon(newimg);
        lblImagen.setIcon(newIcon);
    }

    public Image convertirIicono(Icon i) {
        GraphicsEnvironment ge
                = GraphicsEnvironment.getLocalGraphicsEnvironment();
        GraphicsDevice gd = ge.getDefaultScreenDevice();
        GraphicsConfiguration gc = gd.getDefaultConfiguration();
        BufferedImage image = gc.createCompatibleImage(138, 149);
        Graphics2D g = image.createGraphics();
        i.paintIcon(null, g, 0, 0);
        g.dispose();
        return image;
    }

