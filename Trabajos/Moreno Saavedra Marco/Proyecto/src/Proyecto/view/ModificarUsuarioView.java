    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        try {
            Usuario bean = new Usuario();
            bean.setUsuario(txtUsuario.getText());
            bean.setContraseña(txtContraseña.getText());
            bean.setPaterno(txtPaterno.getText());
            bean.setMaterno(txtMaterno.getText());
            bean.setNombre(txtNombre.getText());
            bean.setDni(txtDNI.getText());
            bean.setDireccion(txtDireccion.getText());
            bean.setTelefono(txtTelefono.getText());
            bean.setEmail(txtEmail.getText());
            bean.setTipo(txtTipo.getText());
            bean.setImagen(convertirIicono(lblImagen.getIcon()));
            AccesoController control = new AccesoController();
            control.grabar(bean, accion, rutaimagen);
            Memoria.put("bean", bean);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnProcesarActionPerformed

    private void btnCambiarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarImagenActionPerformed
        JFileChooser jfc = new JFileChooser();
        jfc.setFileFilter(filter);
        int opcion = jfc.showOpenDialog(this);
        if (opcion == JFileChooser.APPROVE_OPTION) {
            String fil = jfc.getSelectedFile().getPath();
            rutaimagen = jfc.getSelectedFile().toString();
            ImageIcon icon = new ImageIcon(fil);
            Image img = icon.getImage();
            Image newimg = img.getScaledInstance(138, 149, java.awt.Image.SCALE_SMOOTH);
            ImageIcon newicon = new ImageIcon(newimg);
            lblImagen.setIcon(newicon);
        }
    }//GEN-LAST:event_btnCambiarImagenActionPerformed

