    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        Usuario bean = new Usuario();
        ModificarUsuarioView view = new ModificarUsuarioView(null, true);
        view.setBean(bean);
        view.accion(Crud.CRUD_NUEVO);
        view.setVisible(true);
        if (Memoria.get("bean") != null) {
            bean = (Usuario) Memoria.get("bean");
            lista.add(0, bean);
            mostrarLista(lista);
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila;
        fila = tblData.getSelectedRow();
        if (fila == -1) {
            return;
        }
        Usuario bean = lista.get(fila);
        ModificarUsuarioView view = new ModificarUsuarioView(null,true);
        view.setBean(bean);
        view.accion(Crud.CRUD_EDITAR);
        view.setVisible(true);
        if (Memoria.get("bean") != null) {
            bean = (Usuario) Memoria.get("bean");
            lista.set(fila, bean);
            mostrarLista(lista);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila;
        fila = tblData.getSelectedRow();
        if (fila == -1) {
            return;
        }
        Usuario bean = lista.get(fila);
        ModificarUsuarioView view = new ModificarUsuarioView(null,true);
        view.setBean(bean);
        view.accion(Crud.CRUD_ELIMINAR);
        view.setVisible(true);
        if (Memoria.get("bean") != null) {
            lista.remove(fila);
            mostrarLista(lista);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

