    private void mostrarLista(List<Usuario> lista) {
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) tblData.getModel();
        tabla.setRowCount(0);
        for (Usuario usuario : lista) {
            Object[] rowData = {
                usuario.getPaterno(),
                usuario.getMaterno(),
                usuario.getNombre(),
                usuario.getDni(),
                usuario.getTelefono()
            };
            tabla.addRow(rowData);
        }

    }

    public void mostrarUsuarios() {
        try {
            //Proceso
            lista = control.readAll();
            //Reporte
            mostrarLista(lista);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
